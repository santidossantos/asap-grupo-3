package grupo3.clase8.ejercicio.integrador;

/**
 * Interfaz que define el comportamiento de un objeto evaluable.
 */
public interface Evaluable {

    /**
     * Método que verifica si un objeto evaluable está aprobado.
     *
     * @return {@code true} si el objeto está aprobado, {@code false} en caso contrario.
     */
    boolean estaAprobado();

}